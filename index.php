<h2>Why do you need developer evangelists?</h2>
<p>Every day millions of dollars are wasted in companies because non-tech people and tech people either don't communicate at all or completely miss each other's points.</p>
<p>Even more money is then spent on internal promotion of your products or external communication and advertising to get people excited about your new product.</p>
<p><strong>What you really need is enthusiasm about your product - honest interest in using it and the right message to the right group.</strong></p>
<p>If your product targets developers this can not be achieved with marketing or PR. We've managed to remove the developer world from the commercial part of IT companies so far over the last few years that developers are very cynical about anything that does not relate to technology first and brand second - and neither marketing nor PR can deliver that.</p>
<p>Developers however are very happy to listen to what other developers say - if these developers are very good, have their respect and deliver the message in the right way.</p>
<p>The trick is to allow people in your company to play with your products and advertise why they are excited about them - after all the people in your company are consumers, too.</p>
<p>The best advertiser is someone who is not consciously advertising a product but instead is so enthusiastic about it that it becomes contagious. You can empower your developers to be these people without much extra cost - all it needs is trust, a bit of time and to let them off the leash.</p>
